export class Fichatecnica {

    /**
     * El identificador de la ficha tecnica
     */
    id:number;
    /**
     * El tipo de vidrios que tiene el automovil que posee la ficha tecnica
     */
    vidrios:string;
    /**
     * Indica si el automovil tiene o no una camara de reversa
     */
    camaraReversa:boolean;
    /**
     * Indica si el automovil tiene o no sensores
     */
    sensores:boolean;
    /**
     * El tipo de rines que tiene el automovil que posee la ficha tecnica
     */
    rines:string;
    /**
     * Indica si el automovil tiene o no aire acondicionado
     */
    aireAcondicionado:boolean;
    /**
     * El numero de airbags que tiene el automovil quer posee la ficha tecnica
     */
    numAirbags:number;
}
